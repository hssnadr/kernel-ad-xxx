<script>
export default {
  data() {
    return {
      animationTrigger: false,
      curMove: '',
      states: [
        // {
        //   id: 'Gard',
        //   state: true
        // },
        {
          id: 'Direct',
          state: false,
        },
        {
          id: 'Uppercut',
          state: false,
        },
        {
          id: 'Fouette',
          state: false,
        },
        {
          id: 'Chasse',
          state: false,
        },
        {
          id: 'FrontKick',
          state: true,
        },
      ],
      wavesState: [
        { id: 0, state: false },
        { id: 1, state: false },
        { id: 2, state: false },
      ],
      isWaving: false,
    }
  },
  methods: {
    getState(id_) {
      const el_ = this.states.find((el) => el.id === id_)
      return el_.state
    },
    randomMove() {
      let ind_ = Math.floor(Math.random() * Math.floor(this.states.length))

      if (this.curMove === this.states[ind_].id) {
        ind_++
        ind_ = ind_ >= this.states.length ? 0 : ind_
      }

      this.playMove(this.states[ind_].id)
      this.curMove = this.states[ind_].id
    },
    playMove(id_) {
      this.states.forEach((el) => {
        el.state = false
      })

      this.setState(id_, true)
      this.animationTrigger = !this.animationTrigger // trig transition animation (reset animation when value change)

      // Waves animation
      if (!this.isWaving) {
        this.isWaving = true // lock wave animation
        this.wavesState.forEach((w_, indx_) => {
          w_.state = false // reset waves
        })
        // Delay each wave enter animation
        setTimeout(() => {
          this.wavesState.forEach((w_, indx_) => {
            setTimeout(() => {
              w_.state = true // delayed show wave
              setTimeout(() => {
                this.isWaving = !(indx_ === this.wavesState.length - 1) // unlock wave animation
              }, 200)
            }, 150 * indx_)
          })
        }, 200)
      }
    },
    setState(id_, state_) {
      this.states.find((el) => el.id === id_).state = state_
    },
  },
}
</script>

